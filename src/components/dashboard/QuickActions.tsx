export default function QuickActions() {
  const [open, setOpen] = useState(false);
  const ref = useRef<HTMLDivElement>(null);
  const router = useRouter();

  useEffect(() => {
    function handleClickOutside(e: MouseEvent) {
      if (ref.current && !ref.current.contains(e.target as Node)) {
        setOpen(false);
      }
    }
    if (open) {
      document.addEventListener("mousedown", handleClickOutside);
      return () => document.removeEventListener("mousedown", handleClickOutside);
    }
  }, [open]);

  return (
    <div className="relative" ref={ref}>
      {/* Trigger button */}
      <button
        onClick={() => setOpen(!open)}
        className="inline-flex items-center gap-1.5 px-3.5 py-2 text-[13px] font-medium text-white bg-[#4F46E5] hover:bg-[#4338CA] rounded-lg transition-colors cursor-pointer"
      >
        <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
          <path strokeLinecap="round" strokeLinejoin="round" d="M12 4.5v15m7.5-7.5h-15" />
        </svg>
        Nouveau
      </button>

      {/* Popover */}
      <AnimatePresence>
        {open && (
          <motion.div
            initial={{ opacity: 0, y: 8, scale: 0.95 }}
            animate={{ opacity: 1, y: 0, scale: 1 }}
            exit={{ opacity: 0, y: 8, scale: 0.95 }}
            transition={{ duration: 0.15 }}
            role="menu"
            aria-label="Actions rapides"
            className="absolute right-0 top-full mt-2 z-50 bg-white rounded-xl border border-[#E6E6E4] shadow-lg w-[320px] max-w-[calc(100vw-2rem)] p-3"
          >
            <p className="text-[11px] font-semibold text-[#999] uppercase tracking-wider px-2 mb-2">
              Actions rapides
            </p>
            <div className="grid grid-cols-2 gap-1.5">
              {actions.map((action) => (
                <button
                  key={action.href}
                  onClick={() => {
                    setOpen(false);
                    router.push(action.href);
                  }}
                  className="flex items-center gap-2.5 p-2.5 rounded-lg text-left hover:bg-[#F7F7F5] transition-colors group cursor-pointer"
                >
                  <span className="text-[#999] group-hover:text-[#4F46E5] transition-colors">
                    {action.icon}
                  </span>
                  <span className="text-[12px] font-medium text-[#1A1A1A] group-hover:text-[#4F46E5] transition-colors">
                    {action.label}
                  </span>
                </button>
              ))}
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  );
}
